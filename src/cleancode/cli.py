"""Command-line interface: ``cleancode check``, ``cleancode rules``, ``cleancode generate``."""

from __future__ import annotations

import itertools
import json
import sys
import threading
import time
from pathlib import Path
from typing import TYPE_CHECKING

import click

from cleancode.config import Config, ConfigError
from cleancode.engine import analyze_path
from cleancode.models import CheckResult, Severity, Violation

if TYPE_CHECKING:
    from cleancode.llm import GenerationResult, ProgressEvent

_SEVERITY_COLORS = {
    Severity.INFO: "cyan",
    Severity.WARNING: "yellow",
    Severity.ERROR: "red",
}


@click.group()
@click.version_option(package_name="cleancode")
def main() -> None:
    """Enforce human-readable Python — especially code generated by LLMs."""


@main.command()
@click.argument(
    "paths", nargs=-1, required=True, type=click.Path(exists=True, path_type=Path)
)
@click.option("--json", "as_json", is_flag=True, help="Emit machine-readable JSON.")
@click.option(
    "--config",
    "config_path",
    type=click.Path(exists=True, path_type=Path),
    help="Explicit TOML config file (default: nearest pyproject.toml).",
)
@click.option(
    "--select", help="Comma-separated rule ids to run exclusively (e.g. ST101,CM302)."
)
@click.option("--ignore", help="Comma-separated rule ids to skip.")
@click.option(
    "--no-suppress", is_flag=True, help="Ignore inline `# cleancode: disable` comments."
)
@click.option(
    "--fail-on",
    type=click.Choice(["info", "warning", "error"]),
    help="Lowest severity that causes a non-zero exit (default from config).",
)
def check(  # cleancode: disable=ST104
    paths: tuple[Path, ...],
    as_json: bool,
    config_path: Path | None,
    select: str | None,
    ignore: str | None,
    no_suppress: bool,
    fail_on: str | None,
) -> None:
    """Analyze Python files and report readability violations."""
    try:
        config = Config.load(paths[0], override=config_path)
        _apply_rule_filters(config, select, ignore)
    except (ConfigError, ValueError) as error:
        raise click.UsageError(str(error)) from error
    if fail_on is not None:
        config.fail_on = Severity.from_name(fail_on)

    file_results: list[CheckResult] = []
    for path in paths:
        file_results.extend(
            analyze_path(path, config, honor_suppressions=not no_suppress)
        )

    if as_json:
        click.echo(
            json.dumps(
                [file_result.to_dict() for file_result in file_results], indent=2
            )
        )
    else:
        _print_human(file_results)

    sys.exit(_exit_code(file_results, config.fail_on))


@main.command("config-template")
@click.option(
    "--out",
    "out_path",
    type=click.Path(path_type=Path),
    help="Write the template to a file instead of stdout.",
)
def config_template(out_path: Path | None) -> None:
    """Print a commented reference config listing every rule and its options."""
    from cleancode.template import build_config_template

    text = build_config_template()
    if out_path is not None:
        out_path.write_text(text, encoding="utf-8")
        click.echo(f"wrote {out_path}", err=True)
    else:
        click.echo(text, nl=False)


@main.command()
def rules() -> None:
    """List every rule with its default threshold and severity."""
    from cleancode.rules import ALL_RULES

    for rule in ALL_RULES:
        defaults = ", ".join(
            f"{key}={value}" for key, value in rule.default_options.items()
        )
        header = f"{rule.id}  {rule.name}"
        click.echo(
            f"{click.style(header, bold=True)}  "
            f"[{rule.default_severity.name.lower()}]  ({defaults})"
        )
        click.echo(f"    {rule.description}\n")


@main.command()
@click.argument("task")
@click.option(
    "--out",
    "out_path",
    type=click.Path(path_type=Path),
    help="Write the final code to a file instead of stdout.",
)
@click.option(
    "--via",
    type=click.Choice(["anthropic", "claude-code"]),
    default="claude-code",
    show_default=True,
    help="Backend: 'claude-code' shells out to the `claude` CLI and reuses its login (Pro/Max subscription); 'anthropic' needs ANTHROPIC_API_KEY.",
)
@click.option(
    "--model",
    default=None,
    help="Model id (defaults to claude-sonnet-5 for anthropic, the CLI's default for claude-code).",
)
@click.option(
    "--max-iterations",
    type=click.IntRange(min=0),
    default=3,
    show_default=True,
    help="Refinement rounds after the initial generation.",
)
@click.option(
    "--show-iterations",
    is_flag=True,
    help="Print each iteration's code to stderr, not just its violation count.",
)
def generate(  # cleancode: disable=ST104
    task: str,
    out_path: Path | None,
    via: str,
    model: str | None,
    max_iterations: int,
    show_iterations: bool,
) -> None:
    """Generate code for TASK with an LLM, re-prompting until it passes all rules."""
    from cleancode.llm import generate_clean_code

    config = Config.load(Path.cwd())
    try:
        client = _build_client(via, model)
    except Exception as error:  # missing key/CLI surfaces as a clean usage error
        raise click.UsageError(str(error)) from error

    backend = via + (f" · {model}" if model else "")
    click.echo(click.style(f"▸ generating clean code for: {task}", bold=True), err=True)
    click.echo(f"  backend: {backend}, up to {max_iterations + 1} attempt(s)\n", err=True)

    reporter = _LiveProgress()
    try:
        generation = generate_clean_code(
            task,
            client,
            config,
            max_iterations=max_iterations,
            on_progress=reporter,
        )
    except Exception as error:  # backend failure mid-loop: report cleanly, no traceback
        reporter.close()
        click.echo(f"\n{click.style('generation failed', fg='red')}: {error}", err=True)
        sys.exit(1)
    reporter.close()

    if show_iterations:
        for index, iteration in enumerate(generation.iterations):
            click.echo(f"\n--- iteration {index} code ---", err=True)
            click.echo(iteration.code, err=True)

    click.echo("\n" + _final_status(generation), err=True)
    if out_path is not None:
        out_path.write_text(generation.code + "\n", encoding="utf-8")
        click.echo(f"wrote {out_path}", err=True)
    else:
        click.echo(generation.code)
    sys.exit(0 if generation.clean else 1)


def _build_client(via: str, model: str | None):
    """Construct the requested LLM backend, applying its default model."""
    from cleancode.llm import AnthropicClient, ClaudeCodeClient

    if via == "anthropic":
        return AnthropicClient(model=model) if model else AnthropicClient()
    return ClaudeCodeClient(model=model)


class _Spinner:
    """A one-line stderr spinner shown while a blocking step runs.

    Animates only on a real TTY; when stderr is piped or captured (CI, tests) it
    prints the label once and stays silent, so machine-readable output and test
    assertions are never touched by escape codes.
    """

    _FRAMES = "⠋⠙⠹⠸⠼⠴⠦⠧⠇⠏"

    def __init__(self, label: str) -> None:
        self._label = label
        self._animated = sys.stderr.isatty()
        self._stop_event = threading.Event()
        self._thread: threading.Thread | None = None

    def start(self) -> None:
        if not self._animated:
            click.echo(f"{self._label}…", err=True)
            return
        self._thread = threading.Thread(target=self._animate, daemon=True)
        self._thread.start()

    def stop(self) -> None:
        if self._thread is None:
            return
        self._stop_event.set()
        self._thread.join()
        sys.stderr.write("\r\033[K")  # carriage return, then clear to end of line
        sys.stderr.flush()

    def _animate(self) -> None:
        started = time.monotonic()
        for frame in itertools.cycle(self._FRAMES):
            elapsed = int(time.monotonic() - started)
            sys.stderr.write(f"\r{frame} {self._label} ({elapsed}s)")
            sys.stderr.flush()
            if self._stop_event.wait(0.15):
                return


class _LiveProgress:
    """``on_progress`` callback that spins during the model call and logs results."""

    def __init__(self) -> None:
        self._spinner: _Spinner | None = None

    def __call__(self, event: ProgressEvent) -> None:
        self._clear()
        label = f"  [{event.iteration}]"
        if event.phase == "generating":
            self._spinner = _Spinner(f"{label} {event.message}")
            self._spinner.start()
        elif event.phase == "checked":
            symbol = click.style("✓", fg="green") if event.ok else "→"
            click.echo(f"{label} {symbol} {event.message}", err=True)
        else:
            click.echo(f"{label} {event.message}…", err=True)

    def _clear(self) -> None:
        if self._spinner is not None:
            self._spinner.stop()
            self._spinner = None

    def close(self) -> None:
        """Stop any spinner left running (e.g. if the loop raised mid-call)."""
        self._clear()


def _final_status(generation: GenerationResult) -> str:
    """Closing summary line: why the loop stopped and whether the code is clean."""
    rounds = len(generation.iterations)
    outcome = click.style("clean", fg="green") if generation.clean else click.style(
        "still has violations", fg="yellow"
    )
    return f"stopped ({generation.stop_reason}) after {rounds} attempt(s) — {outcome}"


def _apply_rule_filters(config: Config, select: str | None, ignore: str | None) -> None:
    if select:
        _apply_select(config, select)
    if ignore:
        _apply_ignore(config, ignore)


def _apply_select(config: Config, select: str) -> None:
    selected = {rule_id.strip() for rule_id in select.split(",")}
    unknown = selected - config.rules.keys()
    if unknown:
        raise ConfigError(f"--select: unknown rule id(s) {', '.join(sorted(unknown))}")
    for rule_id, rule_config in config.rules.items():
        rule_config.enabled = rule_id in selected


def _apply_ignore(config: Config, ignore: str) -> None:
    for rule_id in (part.strip() for part in ignore.split(",")):
        if rule_id not in config.rules:
            raise ConfigError(f"--ignore: unknown rule id {rule_id}")
        config.rules[rule_id].enabled = False


def _print_human(file_results: list[CheckResult]) -> None:
    counts = {severity: 0 for severity in Severity}
    for file_result in file_results:
        _print_file_result(file_result, counts)
    _print_summary(file_results, counts)


def _print_file_result(file_result: CheckResult, counts: dict[Severity, int]) -> None:
    if file_result.parse_error:
        click.echo(
            f"{file_result.path}: "
            f"{click.style('syntax error', fg='red')}: {file_result.parse_error}"
        )
        return
    for violation in file_result.violations:
        counts[violation.severity] += 1
        _print_violation(file_result.path, violation)


def _print_violation(path: str, violation: Violation) -> None:
    severity_label = click.style(
        violation.severity.name.lower(), fg=_SEVERITY_COLORS[violation.severity]
    )
    click.echo(
        f"{path}:{violation.line}:{violation.col}: {severity_label} "
        f"{violation.rule_id} [{violation.rule_name}] {violation.message}"
    )
    if violation.suggestion:
        click.echo(f"    fix: {violation.suggestion}")


def _print_summary(file_results: list[CheckResult], counts: dict[Severity, int]) -> None:
    total = sum(counts.values())
    files = len(file_results)
    if total == 0 and not any(file_result.parse_error for file_result in file_results):
        click.echo(click.style(f"✓ {files} file(s) clean", fg="green"))
    else:
        click.echo(
            f"\n{total} violation(s) in {files} file(s): "
            f"{counts[Severity.ERROR]} error(s), {counts[Severity.WARNING]} warning(s), "
            f"{counts[Severity.INFO]} info(s)"
        )


def _exit_code(file_results: list[CheckResult], fail_on: Severity) -> int:
    if any(file_result.parse_error for file_result in file_results):
        return 2
    worst = max(
        (
            violation.severity
            for file_result in file_results
            for violation in file_result.violations
        ),
        default=None,
    )
    return 1 if worst is not None and worst >= fail_on else 0
